export class RateChange {

    private rateChangeDateStr;
    private percentage;

    public getRateChangeDateStr() {
        return this.rateChangeDateStr;
    }
    public setRateChangeDateStr(rateChangeDate) {
        this.rateChangeDateStr = rateChangeDate;
    }
    public getPercentage() {
        return this.percentage;
    }
    public setPercentage(percentage) {
        this.percentage = percentage;
    }

}
